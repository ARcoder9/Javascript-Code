// Primitive Data Types: These are immutable, meaning their values cannot be changed after creation.
// Number: Represents both integer and floating-point numbers.

// String: Represents sequences of characters, enclosed in single quotes, double quotes, or backticks (for template literals).

// Boolean: Represents logical entities with two possible values: true or false.

// Undefined: Represents a variable that has been declared but not yet assigned a value.

// Null: Represents the intentional absence of any object value. It is a primitive value.

// BigInt: Represents whole numbers larger than 2^53 - 1, the maximum number that can be represented by the Number type.

// Symbol: Represents a unique identifier. Symbols are primarily used as object property keys to avoid naming collisions.


// Non-Primitive (Reference) Data Type:
// Object: A complex data type that allows you to store collections of data and more complex entities. Objects are mutable, meaning their properties can be changed after creation. This category includes:

// Arrays: Ordered collections of values, accessed by index.

// Functions: Callable objects that encapsulate reusable blocks of code.

